# Logarithm / exponent tables for GF(2^8) with poly 0x1C3 (constant-time)
_GF_LOG = [0] * 256
_GF_EXP = [0] * 512
_x = 1
for _i in range(255):
    _GF_EXP[_i] = _x
    _GF_LOG[_x] = _i
    _x <<= 1
    if _x & 0x100:
        _x ^= 0x1C3
for _i in range(255, 512):
    _GF_EXP[_i] = _GF_EXP[_i - 255]

def _gf_mul(a: int, b: int) -> int:
    """Умножение в GF(2^8), constant-time."""
    if a == 0 or b == 0:
        return 0
    return _GF_EXP[_GF_LOG[a] + _GF_LOG[b]]

def _s(state: bytes) -> bytes:
    return bytes(_SBOX[byte] for byte in state)

def _p(state: bytes) -> bytes:
    return bytes(state[_TAU[i]] for i in range(64))

def _l(state: bytes) -> bytes:
    res = bytearray(64)
    for row in range(8):
        base = row * 8
        row_bytes = state[base:base+8]
        for col in range(8):
            s = 0
            for k in range(8):
                s ^= _gf_mul(_A[row][k], row_bytes[k])
            res[base + col] = s
    return bytes(res)

def _lps(block: bytes) -> bytes:
    return _l(_p(_s(block)))

def _lpsx(key: bytes, block: bytes) -> bytes:
    return _lps(bytes(a ^ b for a, b in zip(block, key)))

# Итерационные константы C[0..11] (64 байта каждая) – вычисляем динамически для точности
def _compute_C():
    C = [bytes(64)]  # C0 = 0^512
    for _ in range(11):
        C.append(_lps(C[-1]))
    return C

_C = _compute_C()

def _g(h: bytes, N: bytes, m: bytes) -> bytes:
    K = bytearray(h)
    for i in range(12):
        N_xor_C = bytes(a ^ b for a, b in zip(N, _C[i]))
        K = _lpsx(K, N_xor_C)
        K = _lps(K)
        K = _lpsx(K, m)
        K = _lps(K)
    return bytes(a ^ b ^ c for a, b, c in zip(K, h, m))

class Streebog:
    def __init__(self, digest_bits: int = 256):
        if digest_bits not in (256, 512):
            raise ValueError("digest_bits must be 256 or 512")
        self.digest_bits = digest_bits
        if digest_bits == 512:
            self.h = bytes(64)
        else:
            self.h = b'\x01' * 64
        self.N = bytes(64)
        self.Sigma = bytes(64)
        self.buf = b''
        self.buf_len = 0

    def update(self, data: bytes) -> 'Streebog':
        self.buf += data
        self.buf_len += len(data)
        while len(self.buf) >= 64:
            block = self.buf[:64]
            self.buf = self.buf[64:]
            self._process_block(block)
        return self

    def _process_block(self, block: bytes):
        old_h = self.h
        N_int = int.from_bytes(self.N, 'big') + 512
        self.N = N_int.to_bytes(64, 'big')
        Sigma_int = int.from_bytes(self.Sigma, 'big') + int.from_bytes(block, 'big')
        self.Sigma = Sigma_int.to_bytes(64, 'big')
        self.h = _g(old_h, self.N, block)

    def digest(self) -> bytes:
        total_bits = self.buf_len * 8
        tail = bytearray(self.buf)
        tail.append(0x80)
        while len(tail) % 64 != 56:
            tail.append(0x00)
        tail.extend(total_bits.to_bytes(8, 'big'))
        for i in range(0, len(tail), 64):
            self._process_block(tail[i:i+64])

        h = _g(self.h, self.N, bytes(64))
        h = _g(h, bytes(64), self.Sigma)
        return h[:self.digest_bits // 8]

    def hexdigest(self) -> str:
        return self.digest().hex()