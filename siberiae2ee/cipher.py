# Logarithm / exponent tables for GF(2^8) (same poly as in hash)
_GF_LOG = [0] * 256
_GF_EXP = [0] * 512
_x = 1
for _i in range(255):
    _GF_EXP[_i] = _x
    _GF_LOG[_x] = _i
    _x <<= 1
    if _x & 0x100:
        _x ^= 0x1C3
for _i in range(255, 512):
    _GF_EXP[_i] = _GF_EXP[_i - 255]

def _gf_mul(a, b):
    if a == 0 or b == 0:
        return 0
    return _GF_EXP[_GF_LOG[a] + _GF_LOG[b]]

# Константы для линейного преобразования L (вектор l)
_L_VEC = [148, 32, 133, 16, 194, 192, 1, 251, 1, 192, 194, 16, 133, 32, 148, 1]

def _l_transform(block):
    res = [0]*16
    for i in range(16):
        s = 0
        for j in range(16):
            s ^= _gf_mul(_L_VEC[(16 - i + j) % 16], block[j])
        res[i] = s
    return bytes(res)

