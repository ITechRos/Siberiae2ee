class Kuznechik:
    BLOCK_SIZE = 16
    KEY_SIZE = 32

    def __init__(self, key: bytes):
        assert len(key) == self.KEY_SIZE
        self._key = key
        self._round_keys = self._expand_key(key)

    def _iter_key(self, k1, k2, const):
        x = bytes(a ^ b for a, b in zip(k1, const))
        x = bytes(_PI[b] for b in x)
        x = _l_transform(x)
        new_k2 = bytes(a ^ b for a, b in zip(x, k2))
        return new_k2, k1

    def _expand_key(self, key):
        k1 = key[:16]
        k2 = key[16:]
        round_keys = [k1, k2]
        for i in range(4):
            k3, k4 = self._iter_key(k1, k2, _C_ITER[2*i])
            k5, k6 = self._iter_key(k3, k4, _C_ITER[2*i+1])
            round_keys.append(k5)
            round_keys.append(k6)
            k1, k2 = k5, k6
        return round_keys[:10]

    def _encrypt_block(self, block):
        state = bytearray(block)
        for rk in self._round_keys[:-1]:
            state = bytearray(a ^ b for a, b in zip(state, rk))
            state = bytearray(_PI[b] for b in state)
            state = bytearray(_l_transform(state))
        state = bytearray(a ^ b for a, b in zip(state, self._round_keys[-1]))
        return bytes(state)

    def _decrypt_block(self, block):
        state = bytearray(block)
        state = bytearray(a ^ b for a, b in zip(state, self._round_keys[-1]))
        for rk in reversed(self._round_keys[:-1]):
            state = bytearray(_l_transform(state))
            state = bytearray(_PI_INV[b] for b in state)
            state = bytearray(a ^ b for a, b in zip(state, rk))
        return bytes(state)

    def encrypt(self, data: bytes) -> bytes:
        assert len(data) % 16 == 0
        return b''.join(self._encrypt_block(data[i:i+16]) for i in range(0, len(data), 16))

    def decrypt(self, data: bytes) -> bytes:
        assert len(data) % 16 == 0
        return b''.join(self._decrypt_block(data[i:i+16]) for i in range(0, len(data), 16))